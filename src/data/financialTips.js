export const getTips = (language = 'en') => {
  return financialTips[language] || financialTips.en;
};

export const getTipsByCategory = (category, language = 'en') => {
  const tips = getTips(language);
  if (!category || category === 'All' || category === 'Tümü') return tips;
  return tips.filter(tip => tip.category === category);
};

export const getCategories = (language = 'en') => {
  const tips = getTips(language);
  return [...new Set(tips.map(tip => tip.category))];
};
